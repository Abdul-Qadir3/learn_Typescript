var num1 = 10;
var num2 = 15;
var result = num1 + num2; // Addition is an operator
console.log("Addition is an operator ".concat(result));
var result1 = num1 - num2; // Subtraction is an operator
console.log("Subtraction is an operator ".concat(result1));
var result2 = num1 * num2; // Multiplication is an operator
console.log("Multiplication is an operator ".concat(result2));
var result3 = num1 / num2; // Division is an operator
console.log("Division is an operator ".concat(result3));
var result4 = num1 % num2; // Modulus is an operator
console.log("Modulus is an operator ".concat(result4));
var result5 = Math.pow(num1, num2); // Exponent is an operator
console.log("Exponent is an operator ".concat(result5));
