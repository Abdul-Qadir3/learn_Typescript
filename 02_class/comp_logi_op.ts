// Comparison operator
n =5;
console.log(n==5);//is n is Equals to 5= True
console.log(n===5);//type checking and value checking
console.log(n!=5);// not equal to False
console.log(n>8);// greater then False
console.log(n<8);// less then True
console.log(n>=8);// greater the or equal to False
console.log(n==8);//less than or equal to True

//Logical Operator &&=AND ||=OR
//AND=True if all conditions are true
//OR=True if one condition is true
n=5;
console.log(n>=5 && n<10);//All condtions must be true to get TRUE
console.log(n>=5 || n<10);//any condtion true result will be true
console.log(n>5 && n <10);//false
console.log(n>=5 || n<10);//true
console.log(n>5 || n<10);//true
console.log(!(n<10));//false
console.log(!(n>10));//True

// for both logical and comparative operator both will return result in boolean